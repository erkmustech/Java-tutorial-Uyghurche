package day33_Class_Object_Constructor;

public class Student {
	
	String name;
	int age;
	char gender;
	int year;
	String university;
	public Student(String name, int age, char gender, int year, String university) {
		super();
		this.name = name;
		this.age = age;
		this.gender = gender;
		this.year = year;
		this.university = university;
	}
	
	
	
	

}
